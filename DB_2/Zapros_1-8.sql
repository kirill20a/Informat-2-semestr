-- ==================================================
-- ЗАПРОС 1: Количество всех студентов
-- ==================================================
SELECT COUNT(*) AS Всего_студентов FROM Студенты;

-- ==================================================
-- ЗАПРОС 2: Количество студентов по направлениям
-- ==================================================
SELECT н.название, COUNT(с.id) AS Количество
FROM Студенты с
JOIN Направления н ON с.id_направления = н.id
GROUP BY н.название;

-- ==================================================
-- ЗАПРОС 3: Количество студентов по формам обучения
-- ==================================================
SELECT ф.название, COUNT(с.id) AS Количество
FROM Студенты с
JOIN Формы_обучения ф ON с.id_формы = ф.id
GROUP BY ф.название;
-- ==================================================
-- ЗАПРОС 4: Макс, мин, средний балл по направлениям
-- ==================================================
SELECT н.название,
       MAX(с.средний_балл) AS Макс_балл,
       MIN(с.средний_балл) AS Мин_балл,
       ROUND(AVG(с.средний_балл), 2) AS Средний_балл
FROM Студенты с
JOIN Направления н ON с.id_направления = н.id
GROUP BY н.название;
-- ==================================================
-- ЗАПРОС 5: Средний балл по направлениям, уровням и формам
-- ==================================================
SELECT н.название AS Направление,
       у.название AS Уровень,
       ф.название AS Форма,
       ROUND(AVG(с.средний_балл), 2) AS Средний_балл
FROM Студенты с
JOIN Направления н ON с.id_направления = н.id
JOIN Уровни_образования у ON с.id_уровня = у.id
JOIN Формы_обучения ф ON с.id_формы = ф.id
GROUP BY н.название, у.название, ф.название;

-- ==================================================
-- ЗАПРОС 6: Топ-5 студентов (Прикладная Информатика, очная)
-- ==================================================
SELECT с.фамилия, с.имя, с.отчество, с.средний_балл
FROM Студенты с
JOIN Направления н ON с.id_направления = н.id
JOIN Формы_обучения ф ON с.id_формы = ф.id
WHERE н.название = 'Прикладная Информатика' AND ф.название = 'очная'
ORDER BY с.средний_балл DESC
LIMIT 5;

-- ==================================================
-- ЗАПРОС 7: Однофамильцы (строгое совпадение строк)
-- ==================================================
SELECT фамилия, COUNT(*) AS количество
FROM Студенты
GROUP BY фамилия
HAVING COUNT(*) > 1;
--===================================================
-- ЗАПРОС 7 (расширенный: Кузнецов и Кузнецова считаются однофамильцами)
SELECT 
    CASE 
        WHEN фамилия LIKE '%а' THEN SUBSTR(фамилия, 1, LENGTH(фамилия) - 1)
        ELSE фамилия
    END AS основа_фамилии,
    GROUP_CONCAT(фамилия) AS варианты,
    COUNT(*) AS количество
FROM Студенты
GROUP BY основа_фамилии
HAVING COUNT(*) > 1;
--===================================================
-- Запрос 8 с сообщением об отсутствии тёзок
--===================================================
SELECT фамилия, имя, отчество, COUNT(*) AS количество
FROM Студенты
GROUP BY фамилия, имя, отчество
HAVING COUNT(*) > 1

UNION ALL

SELECT 'Нет полных тезок', '', '', 0
WHERE NOT EXISTS (
    SELECT 1 FROM Студенты
    GROUP BY фамилия, имя, отчество
    HAVING COUNT(*) > 1
    LIMIT 1
);